## if statements are used to make decisions in code. They allow you to execute certain blocks of code based on conditions.
# Example of if statement
age = 10
if age >= 18:
    print("You are a grown as nigga")
# In this example, the if statement checks if the variable age is greater than or equal to 18.

elif age <18 and age >= 13:
    print("You are a teenager")
# In this example, the elif statement checks if the variable age is less than 18 and greater than or equal to 13.

else:
    print ("You are a child")
# In this example, the else statement is executed if none of the previous conditions are true, meaning that age is less than 13.

## Note: Indentation is important in Python.
# The code block inside the if, elif, and else statements must be indented to indicate that they belong to that particular condition.

#Comparison operators are used inside if statements to compare values and make decisions based on those comparisons.
# Example of comparison operators in if statements

## (==) # Equal to: Checks if two values are equal.
## (!=) # Not equal to: Checks if two values are not equal.
## (>)  # Greater than: Checks if the left value is greater than the right value.
## (<)  # Less than: Checks if the left value is less than the right value.
## (>=) # Greater than or equal to: Checks if the left value is greater than or equal to the right value.
## (<=) # Less than or equal to: Checks if the left value is less than or equal to the right value.

