## A string is a sequence of characters. In Python, strings are enclosed in either single quotes (' ') or double quotes (" ").
# Example of a string
name = "Alice"
greeting = 'Hello, ' + name + '!'
#print(greeting)  # Output: Hello, Alice!

#integer is a whole number without a decimal point. In Python, integers can be positive, negative, or zero.
# Example of an integer
age = 30
print(age)  # Output: 30

# A float is a number that has a decimal point. In Python, floats can also be positive, negative, or zero.
height = 5.9
print(height)  # Output: 5.9   

# A boolean is a data type that can only have two values: True or False. In Python, booleans are often used in conditional statements and logical operations.
is_student = True
print(is_student)  # Output: True

#multiple assignments = allows you to assign values to multiple variables in a single line of code.
age = 25   
Attractive = True
name = "Bob"

print(name)  # Output: Bob
print(age)   # Output: 25
print(Attractive)  # Output: True

